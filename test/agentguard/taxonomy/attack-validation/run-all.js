#!/usr/bin/env node
/**
 * @file run-all.js
 * @description Master test runner — executes ALL attack scripts sequentially
 * and generates a summary report (Markdown table + JSON).
 *
 * Usage:
 *   node run-all.js          # Run all attacks
 *   node run-all.js --quick  # Run only first 5 attacks
 */

import { readdirSync, readFileSync, writeFileSync, mkdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import { execSync } from 'child_process';
import chalk from 'chalk';
import { getModelInfo } from './lib/api.js';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ATTACKS_DIR = join(__dirname, 'attacks');
const RESULTS_DIR = join(__dirname, 'results');

mkdirSync(RESULTS_DIR, { recursive: true });

const isQuick = process.argv.includes('--quick');

// Discover all attack scripts, sorted by filename
const scripts = readdirSync(ATTACKS_DIR)
  .filter(f => f.endsWith('.js') && /^\d{2}-/.test(f))
  .sort();

const selected = isQuick ? scripts.slice(0, 5) : scripts;

console.log(chalk.bold.magenta(`
╔══════════════════════════════════════════════════════════════╗
║        AI Attack Validation Suite — Full Test Run           ║
╚══════════════════════════════════════════════════════════════╝
`));

const modelInfo = getModelInfo();
console.log(chalk.gray(`  Model: ${modelInfo.model} (${modelInfo.provider})`));
console.log(chalk.gray(`  Attacks: ${selected.length} scripts${isQuick ? ' (quick mode)' : ''}`));
console.log(chalk.gray(`  Started: ${new Date().toISOString()}\n`));

const allResults = [];

for (const script of selected) {
  const scriptPath = join(ATTACKS_DIR, script);
  console.log(chalk.yellow(`\n▶ Running: ${script}...`));

  try {
    // Run each attack as a subprocess to isolate failures
    execSync(`node "${scriptPath}"`, {
      cwd: __dirname,
      stdio: 'inherit',
      timeout: 120000, // 2 min per attack
      env: { ...process.env },
    });

    // Read results JSON if it was saved
    const resultId = script.replace('.js', '');
    try {
      const resultPath = join(RESULTS_DIR, `${resultId}.json`);
      const data = JSON.parse(readFileSync(resultPath, 'utf-8'));
      allResults.push({
        id: resultId,
        name: data.name,
        total: data.summary.total,
        succeeded: data.summary.succeeded,
        rate: data.summary.rate,
        paperProbability: data.paperProbability || 'N/A',
      });
    } catch {
      allResults.push({ id: script, name: script, total: 0, succeeded: 0, rate: 0, paperProbability: 'N/A' });
    }
  } catch (err) {
    console.log(chalk.red(`  ✗ Failed: ${err.message}`));
    allResults.push({ id: script, name: script, total: 0, succeeded: 0, rate: 0, paperProbability: 'N/A', error: err.message });
  }
}

// Generate summary
const totalVariants = allResults.reduce((s, r) => s + r.total, 0);
const totalSucceeded = allResults.reduce((s, r) => s + r.succeeded, 0);
const overallRate = totalVariants > 0 ? (totalSucceeded / totalVariants * 100).toFixed(1) : '0.0';

console.log(chalk.bold.magenta(`
╔══════════════════════════════════════════════════════════════╗
║                    SUMMARY REPORT                           ║
╚══════════════════════════════════════════════════════════════╝
`));

console.log(chalk.bold(`  Model: ${modelInfo.model}`));
console.log(chalk.bold(`  Total attacks: ${allResults.length}`));
console.log(chalk.bold(`  Total variants: ${totalVariants}`));
console.log(chalk.bold(`  Total bypassed: ${chalk.red(totalSucceeded)} / ${totalVariants} (${overallRate}%)`));
console.log();

// Markdown table
const mdLines = [
  `# Attack Validation Report`,
  '',
  `**Model:** ${modelInfo.model} (${modelInfo.provider})  `,
  `**Date:** ${new Date().toISOString()}  `,
  `**Overall:** ${totalSucceeded}/${totalVariants} bypassed (${overallRate}%)  `,
  '',
  '| # | Attack | Variants | Bypassed | Rate | Paper P(success) |',
  '|---|--------|----------|----------|------|------------------|',
  ...allResults.map((r, i) =>
    `| ${i + 1} | ${r.name} | ${r.total} | ${r.succeeded} | ${r.rate}% | ${r.paperProbability}/5 |`
  ),
  '',
  '## Risk Assessment',
  '',
  `Total attack categories tested: ${allResults.length}  `,
  `Total variants tested: ${totalVariants}  `,
  `Overall bypass rate: ${overallRate}%  `,
  '',
  '### High-Risk Attacks (>50% bypass rate)',
  '',
  ...allResults.filter(r => r.rate > 50).map(r => `- **${r.name}**: ${r.rate}% bypass rate`),
  allResults.filter(r => r.rate > 50).length === 0 ? '- None detected ✓' : '',
  '',
  '### Recommendations',
  '',
  '- Review any attack with >0% bypass rate for specific failure modes',
  '- Cross-reference results with paper\'s predicted probabilities',
  '- Re-test after model updates or prompt changes',
  '',
  `*Generated by AI Attack Validation Suite*`,
];

const summaryMd = mdLines.join('\n');
writeFileSync(join(RESULTS_DIR, 'SUMMARY.md'), summaryMd);
writeFileSync(join(RESULTS_DIR, 'summary.json'), JSON.stringify({
  modelInfo,
  timestamp: new Date().toISOString(),
  overall: { totalAttacks: allResults.length, totalVariants, totalSucceeded, overallRate: parseFloat(overallRate) },
  attacks: allResults,
}, null, 2));

console.log(chalk.green(`\n  ✓ Report saved to results/SUMMARY.md and results/summary.json\n`));
